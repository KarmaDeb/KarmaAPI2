package es.karmadev.api.minecraft.bossbar.component;

/**
 * BossBar flag
 */
public enum BarFlag {
    /**
     * Dark the player screen while the boss
     * bar is active
     */
    DARKEN_SCREEN,
    /**
     * Play boss music while the boss bar is
     * active
     */
    PLAY_BOSS_MUSIC,
    /**
     * Add fog to the world while the boss
     * bar is active
     */
    CREATE_WORLD_FOG
}
