package es.karmadev.api.minecraft.text.component.text.click;

/**
 * Represents a click action
 */
public enum ClickAction {
    /**
     * Opens a URL
     */
    OPEN_URL,
    /**
     * Copies a text
     */
    COPY_TEXT,
    /**
     * Writes a text
     */
    WRITE_TEXT,
    /**
     * Executes a command
     */
    EXECUTE
}
