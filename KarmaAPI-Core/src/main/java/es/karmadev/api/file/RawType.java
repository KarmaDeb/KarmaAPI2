package es.karmadev.api.file;

/**
 * Raw file value types
 */
public enum RawType {
    /**
     * String
     */
    STRING,
    /**
     * Character
     */
    CHARACTER,
    /**
     * Number
     */
    NUMBER,
    /**
     * Boolean
     */
    BOOLEAN,
    /**
     * Serializable
     */
    SERIALIZABLE,
    /**
     * Object
     */
    OBJECT,
    /**
     * Invalid object
     */
    NULL
}
