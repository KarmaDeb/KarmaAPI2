package es.karmadev.api.logger;

/**
 * Karma logging level
 */
public enum LogLevel {
    /**
     * Non important debug message
     */
    DEBUG,
    /**
     * Important debug message
     */
    DEBUG_SEVERE,
    /**
     * Success message
     */
    SUCCESS,
    /**
     * Informative message
     */
    INFO,
    /**
     * Warning message
     */
    WARNING,
    /**
     * Severe message
     */
    SEVERE,
    /**
     * Error message
     */
    ERROR
}
